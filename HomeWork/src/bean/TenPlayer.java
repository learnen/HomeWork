package bean;

public class TenPlayer {

    /**
     * nickName : xxx
     * score : 11
     */

    private String nickName;
    private int score;
    /**
     * nickname : xxx
     */

    private String nickname;

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }
}
